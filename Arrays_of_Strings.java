public class Arrays_of_strings {
    public static void main(String[] args) {
        int[] numbers = new int[5];
        numbers[0]=10;
        System.out.println(numbers[0]);
        System.out.println(numbers[1]);
        
    }
}
//TERMINAL OUTPUT:
//10
//0


